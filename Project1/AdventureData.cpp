void AdventureData::printCaveData(const CaveData& cave_data)
{
   std::cout << "Printing cave data ..." << std::endl;
   for (const auto& cave_data_row : cave_data)
   {
      for (const auto& cave_data_elem : cave_data_row)
         std::cout << cave_data_elem;

      std::cout << std::endl;
   }
}